let searchBtnElement = document.querySelector('.search-btn');
let itemInputElement = document.querySelector('#item');
let tableElement = document.querySelector('.search-result table');
let searchDivElement = document.querySelector('.search-result');

let availableBusElements;

searchBtnElement.addEventListener('click', function()
{
    let item = itemInputElement.value;
    
    if(item !== "")
    {
        availableBusElements = document.querySelectorAll('.available-dataItem') 
        
        for(let i = 0; i < availableBusElements.length; i++)
        {
            tableElement.style.display = 'none';
            availableBusElements[i].remove();
        }

        

        let found = false;
        data.forEach(function(dataItem)
        {
            if(dataItem.name.toLowerCase() === item.toLowerCase())
            {
                tableElement.style.display = 'inline-block';
                searchDivElement.style.display = 'inline-block';

                found = true;
                console.log('matched')
                let template = `
                <tr class = "available-dataItem">
                    <td><img style="height:50px;width:50px;" src = "${dataItem.image}"></td>
                    <td>${dataItem.name}</td>
                    <td>${dataItem.price}</td>
                    <td><a href="${dataItem.filename}">View</a></td>
                </tr>
                `
                tableElement.insertAdjacentHTML('beforeend', template)
            }
        })
        
        if(!found)
        {
            let template = `
            <h3 class = "available-dataItem">
                No dataItem found!
            </h3>
            `
            searchDivElement.insertAdjacentHTML('beforeend', template)
        }
    }
})
